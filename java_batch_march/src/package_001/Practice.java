package package_001;

public class Practice {

	 //Default values of datatypes 
	//Primitive 
	//Numeric 
	//integer
	
	byte  a;
	short b ;
	int  c;
	long  d;
	
	//floating
	
	float  e;
	double  f;
	
	//Non-numeric
	char g;
	boolean h;
	
	//Non-primitive
	
	String i;
	
	
	

	public static void main(String[] args) {
		Practice p=new Practice();
		System.out.println("The default value of byte is: "+p.a);
		System.out.println("The default value of short is: "+p.b);
		System.out.println("The default value of int is: "+p.c);
		System.out.println("The default value of long is: "+p.d);
		
		
		System.out.println("The default value of float is: "+p.e);
		System.out.println("The default value of double is: "+p.f);
		
		System.out.println("The default value of char is: "+p.g);
		System.out.println("The default value of boolean is: "+p.h);
		
		System.out.println("The default value of string is: "+p.i);






	}

}
