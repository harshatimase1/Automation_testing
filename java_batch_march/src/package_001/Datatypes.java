package package_001;

public class Datatypes {

	public static void main(String[] args) {

         //Dtatypes

		// Integer

		byte a = 30;
		short b = 50;
		int c = 60;
		long d = 90;

		// Floating

		float e = 89.66f;

		double f = 98.23;

		// Non-numeric: Does not contain any number

		char q = 's';

		int w = 90;

		boolean k = (w > 100);

		System.out.println(k);

		// Non-primitive datatypes:It refers to the class object.
		// ex: String,array

		// create object

		Datatypes r = new Datatypes();

		String z = "ABC";

		String s = new String("ABC");

	}

}
