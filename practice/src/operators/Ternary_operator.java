package operators;

public class Ternary_operator {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;

		int x = (a < b) ? a : b;
		System.out.println(x);
		
		System.out.println("--------------------------");
		
		int a1 = 10;
		int b1 = 20;

		int x1 = (a1 > b1) ? a1 : b1;
		System.out.println(x1);
	}

}
