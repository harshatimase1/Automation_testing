package loops;

public class While_loop {

	public static void main(String[] args) {

		/*syntax: initialize variable; 
		  while(condition) { bock of code;
		  increment/decrement;} */
		
		int a=50;
		while(a<=75) {
			System.out.print(a+" ");
			a++;
		}
		//System.out.println(" ");
		System.out.println("\n---------------Print Output--------------------------");
		System.out.println("---------------------------------------------------------------------");
		int b=75;
		while(b>=50) {
			System.out.println(b+" ");
			b--;
		}
		

	}

}
