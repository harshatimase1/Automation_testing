package java_control_statement;

public class SwitchInteger {

	public static void main(String[] args) {
		int a = 5;

		switch (a) {
		case 1:
			System.out.println("Odd Number");
			break;
		case 2:
			System.out.println("Even Number");
			break;

		case 3:
			System.out.println("Odd Number");
			break;

		case 4:
			System.out.println("Even Number");
			break;

		case 5:
			System.out.println("Odd Number");
			break;
		default:
			System.out.println("None");
			break;

		}

	}

}
