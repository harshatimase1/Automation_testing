package control_statements;

public class Switch_Statement {

	public static void main(String[] args) {
        
		switch (5) {
		case 1: {

			System.out.println("Output of a is one");
		}

		case 2: {
			System.out.println("Output of a is two");
		}
		case 3: {
			System.out.println("Output of a is three ");

		}

		case 4: {
			System.out.println("Output of a is four ");
		}

		case 5: {
			System.out.println("Output of a is five");
		}
		default:{
			System.out.println("None of the above");
		}
		}
		}}
		
		

	


