package control_statements;

public class If_else_statement {

	public static void main(String[] args) {

		int a = 700;
		int b = 500;
		
		//if condition in If statement is true then execute block of code
		if (a > b) {
			System.out.println("PRINT");

		} else {
			System.out.println("condition");
		}
		//if condition in If statement is false then execute block of code in else statement
		if (500 > b) {
			System.out.println("TRUE");
		} else {
			System.out.println("Output is prined");
		}
	}
}
